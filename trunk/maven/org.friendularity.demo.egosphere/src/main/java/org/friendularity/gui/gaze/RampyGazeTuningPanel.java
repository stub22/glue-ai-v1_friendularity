	private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
		try{
			boolean rampy = myMotionStyle == MotionStyle.RAMPY;
			if(rampy){
				myGJS.rampVelMaxDPS = Double.parseDouble(txtVel.getText());
				myGJS.rampAccelMaxDPSPS = Double.parseDouble(txtAccel.getText());
				myGJS.rampDecelMaxDPSPS = Double.parseDouble(txtDecel.getText());
			}else{
				myGJS.flatMotionWeight = Double.parseDouble(txtFlat.getText());
			}
		}catch(Throwable t){}
	}//GEN-LAST:event_jButton1MouseClicked



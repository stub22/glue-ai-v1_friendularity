	public RampyGazeTuningPanel(GazeJointStrategy gjs, MotionStyle style){
        initComponents();
		myGJS = gjs;
		myMotionStyle = style;
		lblJoint.setText(((Integer)gjs.getLogicalJointID()).toString());
		boolean rampy = style == MotionStyle.RAMPY;
		txtVel.setEnabled(rampy);
		txtAccel.setEnabled(rampy);
		txtDecel.setEnabled(rampy);
		txtFlat.setEnabled(!rampy);
		if(rampy){
			txtVel.setText(gjs.rampVelMaxDPS.toString());
			txtAccel.setText(gjs.rampAccelMaxDPSPS.toString());
			txtDecel.setText(gjs.rampDecelMaxDPSPS.toString());
		}else{
			txtFlat.setText(gjs.flatMotionWeight.toString());
		}
	}

